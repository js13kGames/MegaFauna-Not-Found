export const getTextLength = (string, size) => {
    var needed = [];
    string = string.toUpperCase();
    for (var i = 0; i < string.length; i++) {
        var letter = letters[string.charAt(i)];
        if (letter) {
            needed.push(letter);
        }
    }

    let currX = 0;
    for (i = 0; i < needed.length; i++) {
        letter = needed[i];
        var addX = 0;
        for (var y = 0; y < letter.length; y++) {
            var row = letter[y];
            addX = Math.max(addX, row.length * size);
        }
        currX += size + addX;
    }

    return (currX - size);
};

export function draw(canvas, context, string, size, startX, startY, color = 'white') {
    context.fillStyle = color;
    let blocks = getPositions(canvas, context, string, size, startX, startY);

    for (let i = 0; i < blocks.length; i++) {
        context.fillRect(blocks[i].x, blocks[i].y, blocks[i].width, blocks[i].height);
    }
}

export const getPositions = (canvas, context, string, size, startX, startY) => {
    let blocks = [];
    var needed = [];
    string = string.toUpperCase();
    for (var i = 0; i < string.length; i++) {
        var letter = letters[string.charAt(i)];
        if (letter) {
            needed.push(letter);
        }
    }

    var currX = startX;
    for (i = 0; i < needed.length; i++) {
        letter = needed[i];
        var currY = startY;
        var addX = 0;
        for (var y = 0; y < letter.length; y++) {
            var row = letter[y];
            for (var x = 0; x < row.length; x++) {
                if (row[x]) {
                    blocks.push({
                        x: currX + x * size,
                        y: currY,
                        width: size,
                        height: size
                    });
                }
            }
            addX = Math.max(addX, row.length * size);
            currY += size;
        }
        if (i === (needed.length - 1)) {
            currX += size;
        } else {
            currX += size + addX;
        }
    }

    return blocks;
}
